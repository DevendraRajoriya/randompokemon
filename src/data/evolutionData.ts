/**
 * Check if a Pokemon ID matches the given gender filter
 */
export function matchesGender(id: number, genders: string[]): boolean {
    if (genders.length === 0) return true; // no filter = all match

    const wantMale = genders.includes("Male");
    const wantFemale = genders.includes("Female");
    const wantGenderless = genders.includes("Genderless");

    if (GENDERLESS_IDS.has(id)) return wantGenderless;
    if (FEMALE_ONLY_IDS.has(id)) return wantFemale;
    if (MALE_ONLY_IDS.has(id)) return wantMale;

    // Most Pokemon have both genders
    return wantMale || wantFemale;
}

/**
 * Check if a Pokemon ID matches the given evolution stage filter
 */
export function matchesEvolutionStage(id: number, stages: string[]): boolean {
    if (stages.length === 0) return true;

    if (stages.includes("Unevolved") && UNEVOLVED_IDS.has(id)) return true;
    if (stages.includes("Evolved Once") && EVOLVED_ONCE_IDS.has(id)) return true;
    if (stages.includes("Evolved Twice") && EVOLVED_TWICE_IDS.has(id)) return true;

    return false;
}

/**
 * Check if a Pokemon ID matches the fully evolved filter
 */
export function matchesFullyEvolved(id: number, options: string[]): boolean {
    if (options.length === 0) return true;

    const isFullyEvolved = FULLY_EVOLVED_IDS.has(id);

    if (options.includes("Fully Evolved") && options.includes("Not Fully Evolved")) return true;
    if (options.includes("Fully Evolved")) return isFullyEvolved;
    if (options.includes("Not Fully Evolved")) return !isFullyEvolved;

    return true;
}
